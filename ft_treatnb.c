#include"printf.h"

int	treatnb(int nb, int base)
{
	int	i;

	i = 0;
	if (nb <= 0)
	{
		nb *= -1;
		i++;
	}
	while (nb > 0)
	{
		nb /= base;
		i++;
	}
	return (i);
}
